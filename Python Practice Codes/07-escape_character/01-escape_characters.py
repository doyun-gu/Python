# Line Shifting
print("Hello!\nI am Doyun GU :)")

# '\' 
print("I am 'Doyun GU' and 21 years old")
print('I am "Doyun GU" and 21 years old')

print("I am \"Doyun GU\" and 21 years old")
print('I am \'Doyun GU\' and 21 years old')

# '\\' printout 'File Directory'
print("C:\\Users\\Doyun GU\\Desktop\\PythonWorkSpace")

# 'r' printout whatever it contains
print(r"C:\Users\Doyun GU\Desktop\PythonWorkSpace")

# '\b' works as 'Back Space'. Delete One Space
print("Redd\bApple")

# '\t' works as 'Tab Space'.
# Tab Spacing Setting (VS Code): File - Preference - Settings - Editting: Tab Size
print("Red\tApple")
