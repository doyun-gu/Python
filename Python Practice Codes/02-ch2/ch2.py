# Data Type: Number
print(5)
print(-10)
print(3.14)
print(1000)

# Equation with print
print(5+3)
print(2*8)
print(6/3)
print(3*(3+1))

# Data Type: Character
print('balloon')
print('butterfly')
print('abcdefg')
print("10")
print("python"*3)

# Data Type: Boolean
print(5>10)
print(5<10)
print(not(5>10))
